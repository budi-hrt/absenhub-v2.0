    {
        JatahCuti::findOrFail($id)->delete();
        $this->success('Data jatah cuti berhasil dihapus.');
    }

    public function with(): array
    {
        $daftarJatah = JatahCuti::orderByDesc('tahun')->get();

        return [
            'daftarJatah' => $daftarJatah,
