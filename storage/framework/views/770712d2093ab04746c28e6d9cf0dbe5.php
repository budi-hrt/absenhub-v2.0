    {
        $this->validate([
            'tahun' => 'required|integer|min:2020|max:2099',
            'jatah_cuti' => 'required|integer|min:0|max:365',
        ]);

        JatahCuti::updateOrCreate(
            ['tahun' => $this->tahun],
            ['jatah_cuti' => $this->jatah_cuti]
        );

        $this->showForm = false;
        $this->reset(['tahun', 'jatah_cuti']);
        $this->tahun = now()->year;
        $this->jatah_cuti = 12;
        $this->success('Jatah cuti berhasil disimpan.');
    }

